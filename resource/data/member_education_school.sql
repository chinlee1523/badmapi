CREATE TABLE `education_dict` (
  `id` INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  `name` VARCHAR(50) NOT NULL UNIQUE COMMENT '学历名称',
  `sort` INT DEFAULT 0 COMMENT '排序值'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='学历字典表';

CREATE TABLE `school` (
  `id` INT UNSIGNED AUTO_INCREMENT PRIMARY KEY,
  `name` VARCHAR(100) NOT NULL UNIQUE COMMENT '学校名称',
  `level` VARCHAR(50) DEFAULT NULL COMMENT '学校级别（如985/211/普通本科/专科/中学等）',
  `type` VARCHAR(50) DEFAULT NULL COMMENT '学校类型（如综合/理工/师范/医科/艺术等）',
  `province` VARCHAR(50) DEFAULT NULL COMMENT '省份',
  `city` VARCHAR(50) DEFAULT NULL COMMENT '城市'
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='学校表';

